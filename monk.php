     <p>    Ananda or Anandatheertha as he was known,who 
        happened to be Gautam’s elder brother before his monkhood,went out and there was
        a courtesan or a prostitute in the town. She gave him alms, looked at him –
        a handsome young man, tall and straight. She said, “I hear that monks are looking
        for shelter in many homes. Why don’t you come and stay in my house?” Ananda said, “I have no issue but I must
        ask the Buddha as to where I should stay. ”Then she became really taunting. <br>
         She said, “Oh, you’re going to ask your guru? Go and ask him, let’s see what he says. ”So Ananda brought 
         what he had collected for the day. Gautama was sitting; he came and placed it at his feet. And everybody is supposed to find
        food and shelter wherever they go. Ananda asked, “Like this, this lady is inviting me. Can I stay there? ”Gautama said, 
        “If she’s inviting you with so much affection, you must go stay there. <br> <br>
        ”The townspeople who were sitting here,
        they said, “What? A monk, he’s going to go
        and stay in a prostitute’s home? This is it . This spiritual process has become corrupt” . Gautama looked at them and said,
        “Why are you so worried? The lady is inviting him,
        let him stay. What is the problem? ”They all started getting up and started shouting. He said, “Wait! I am on this 
        path because I see this is the  most powerful way to live. Now you’re telling me her ways are more
        powerful than my ways . If that is the truth, I will also go and join her. I am here because I see this is the most precious
        and powerful way to exist.
        <br> <br>

               <b>If you think her ways are better than mine,
        I should also go and join because if you are a true seeker that’s how it should be. If you found something 
        much higher, you go for that. </b> 
        <br><br> ”Then people shouted and many of them of course leave . Then Ananda went and stayed with her. 
        Monsoon time, because of rains it gets cold,
        monk is wearing a thin habit,so she gave him a nice silk wrap. He covered himself. Then people saw, 
        “See, he’s gone”! She made nice cooking and gave it to him, he ate. Then in the evening, she danced for him. 
        He sat , watching very carefully , with utmost attention. Then when they heard the music,“This is it, finished”.  
        Things went on. When the time was up, when the rains stopped,
        when it’s time to move, Ananda came to Gautama along with a female monk . So, the power of being on the path of truth
        has been there but, has been many times at its height,
        but never got mainstreamed. 
